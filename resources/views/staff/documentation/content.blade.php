    <h2 id="mcetoc_1eepm6ak80">Use GeoCoding Info</h2>
    <p>This is a tool to use information from geocoding (I think it comes from Google Maps if I remember correctly) to
        fill in missing information in listings. It's particularly useful for filling in the Neighborhood/District field
        or the Regions.</p>
    <p>So for example, set Status to "OK", and for the "City" field enter "New York City", then Search.<br/><br/>Some of
        the listings have a neighborhood names, but some don't. You can click the checkbox next to the ones that don't
        have one. And then at the bottom for "Set Listing Field" choose Neighborhood/District. And for "To Geocoding
        Field" choose "neighborhood". So what that will do if you click Submit is for each of the listings that you
        clicked the checkmark on, it will copy the "Geocoding neighborhood" to the listing's "Neighborhood/District"
        field. So it's a fast and powerful way to get location information from the geocoding information. But be
        careful, if you click the wrong options you could accidentally set the Country of a bunch of listings to their
        city names, etc, and that can make a mess of things.</p>
    <h2 id="mcetoc_1eepnmk93e">Import Hostelworld Reviews</h2>
    <p>Since June 2020, we are importing reviews from Hostelworld.</p>
    <p><strong>Example</strong>: <a href="https://dev.hostelz.com/hostel/251134-Ecomama#hostelzratings" target="_blank"
                                    rel="noopener">Ecomama in Amsterdam</a>.</p>
    <p>It does it one at a time as it does the update listing process. So it should get all of them in 1-2 months.</p>
    <ul>
        <li><strong>How many reviews do we import?</strong></li>
    </ul>
    <p>For Hostelworld it imports up to 20. But then when it displays them, it gets the imported reviews from all of the
        booking sites and combines them and sorts them by date. And then it uses the most recent 20 of them from the
        combined list.</p>
    <p><strong>What if we want to change?</strong></p>
    <p>If you wanted to change the number that it uses, we can look for the calls to <em><strong>getImportedReviewsAsRatings</strong></em>
        because the number of reviews to fetch is passed to that function.</p>
