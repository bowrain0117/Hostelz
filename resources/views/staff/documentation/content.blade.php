    <h2 id="mcetoc_1esksm23o0">Manually Import Hostels</h2>
    <p>Usually, we wont need to do manually merge hostels, but just in case this was the process for a brand-new hostel
        that wanted to be listed asap: <a href="https://www.hostelz.com/hostel/+471714-Zeus-is-Loose-Hostel"
                                          target="_blank" rel="noopener noreferrer">https://www.hostelz.com/hostel/+471714-Zeus-is-Loose-Hostel</a>
    </p>
    <ol>
        <li>
            <p>Visit Our Data of the Booking platform (hostelworld, booking or hostelworld) &gt; <a
                        href="../imported?mode=searchAndList&amp;search[status]=active&amp;search[system]=BookHostels"
                        target="_blank" rel="noopener noreferrer">https://secure.hostelz.com/staff/imported?mode=searchAndList&amp;search[status]=active&amp;search[system]=BookHostels</a>
            </p>
        </li>
        <li>
            <p>Press "<strong>Create a New Imported</strong> "</p>
        </li>
        <li>
            <p>Add here relevant information such as:</p>
            <ol>
                <li>Status</li>
                <li>System</li>
                <li>Property Type</li>
                <li>Listing</li>
                <li>Name</li>
                <li>URL Link</li>
                <li><strong>Very important: Integer!!</strong></li>
            </ol>
        </li>
    </ol>
    <p>The Integer is basically the Hostel ID for each platform.</p>
    <p><strong>How to find Booking Hostel ID:</strong></p>
    <ul>
        <li>Booking.com: Source Code &gt; search for "b_hotel_id". This will show you for instance <em>b_hotel_id:
                '<strong>6869744</strong>',<br/>(Sometimes the last part of the URL is the ID too. Please double-check.)<br/></em>
        </li>
        <li>Hostelworld: It is the last part of the URL e.g.
            <em>www.hostelworld.com/.../Selina-Medellin/<strong>277778</strong></em></li>
    </ul>
    <p><strong>Final step</strong></p>
    <p>Check if the Hostel is connected! Need to click "Update listing" to import availability.</p>
