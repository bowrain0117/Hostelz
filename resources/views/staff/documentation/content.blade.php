    <h2 id="mcetoc_1ea53nh6a10">Featured Hostels</h2>
    <p>We have the possibility to influence the listings of each hostel.</p>
    <p>When calculating sorting, different parameters are summed. The Featured listing has a coefficient of 1000 and the
        values Penalized (-1) Normal (0) Featured (1) Featured - Extra Special (2). For example, if you select Featured
        - Extra Special, then 2 * 1000 = 2000 will be higher than listings with a high rating.</p>
    <p>We have four different status for hostels:</p>
    <ol>
        <li><strong>penalized</strong>: -1 * 1000 = -1000 value. Also, a penalized listing cannot be the top-rated
            hostel in a city (e.g. Church Hostel in London)
        </li>
        <li><strong>default</strong>: absolute default, regular listing with no modification</li>
        <li><strong>featured </strong></li>
        <li><strong>extra featured</strong><strong><br/></strong></li>
    </ol>
    <h2 id="mcetoc_1earuqd2v0">Best-Rated Hostel</h2>
    <p>For each city, we have a so-called top-rated hostel. This hostel is usually ranked as #1 in default order. By
        default we order all hostels by <strong>combinedRating</strong> and moveGoodContentListingsToFirstPage by
        <strong>contentscore.</strong></p>
    <p>Sergey found this</p>
    <ul>
        <li>$reviewScore + $ratingsScore = $contentScore</li>
        <li>$reviewScore - number of characters in the reviews (publishedReview)</li>
        <li>$ratingsScore - number of characters in the comments</li>
    </ul>
    <p><strong>What does this mean?</strong></p>
    <p>This means we rate the quality/ quantity of our Hostelz Reviews and the comments. Therefore, it an happen that a
        hostel with the same rating as #1 hostel and more ratings, is only ranked as #2.</p>
